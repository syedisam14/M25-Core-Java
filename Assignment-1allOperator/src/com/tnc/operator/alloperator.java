package com.tnc.operator;

public class alloperator {

	public static void main(String[] args) 
	{
		int a = 10;
	      int b = 20;

	      System.out.println("10 == 20 = " + (a == b) );
	      System.out.println("10 != 20 = " + (a != b) );
	      System.out.println("10 > 20 = " + (a > b) );
	      System.out.println("10 < 20 = " + (a < b) );
	      System.out.println("10 >= 20 = " + (b >= a) );
	      System.out.println("20 <= 10 = " + (b <= a) );
		
	}

}
